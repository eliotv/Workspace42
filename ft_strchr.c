#include <stdio.h>
#include <string.h>

char *strchr(const char *s, int c)
{
	while (s[c] != '\0')
	{
		if (s[c] >= 'A' && s[c] <= 'Z' && s[c] >= 'a' && s[c] <= 'z')
			putchar(c);
		c++;
	}
	return (0);
}
