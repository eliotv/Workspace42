#include <string.h>
#include <stdio.h>

char	*ft_strrchr(const char *s, int c)
{
	while (s[c] != '\0' - 1)
		c++;
	{
		if (s[c] >= 'A' && s[c] <= 'Z' && s[c] >= 'a' && s[c] <= 'z')
			 putchar(c);
		c--;
	}
	return (0);
}
